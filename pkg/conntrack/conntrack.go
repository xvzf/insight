package conntrack

import (
	"github.com/xvzf/insight/pkg/flow"
)

// Event types for a Conntrack update
const (
	EventNew = iota
	EventUpdate
	EventDestroy
)

// Entry contains the two flow-pairs (TCP or UDP of a line in the conntrack output)
type Entry struct {
	FlowMeta0 flow.Meta
	FlowMeta1 flow.Meta
}

// Event contains a conntrack event
type Event struct {
	Type  uint8
	Entry Entry
}
